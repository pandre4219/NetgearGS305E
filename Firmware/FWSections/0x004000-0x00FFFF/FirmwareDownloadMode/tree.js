function transLang() {
var eles = document.getElementsByClassName('lang'),
len = eles.length,
i, key, e;
var langList = ['en','en','zh','fr','de','ja','es'],
langType = top.document.getElementById('langType').value;
langType = !!langList[langType] ? langList[langType] : 'en';
if(langType === 'en') {
document.documentElement.style.opacity = 1;
return;
}
for (i = 0; i < len; i++){
e = eles[i];
if (e.tagName == "INPUT" && e.type == "button") {
e.value = trans(e.value);
} else {
e.innerHTML = trans(e.innerHTML);
}
}
document.documentElement.style.opacity = 1;
}
function trans(str) {
var langList = ['en','en','zh','fr','de','ja','es'],
langType = top.document.getElementById('langType').value;
langType = !!langList[langType] ? langList[langType] : 'en';
if (langType === 'en') {
return str;
}
if (lang[str]) {
return lang[str][langType];
} else {
return str;
}
}
document.addEventListener("DOMContentLoaded", transLang, false);

function checkIPv4Address(field)
{
var obj=field.value;
var exp=/^(\d{1,2}|1\d\d|2[0-4]\d|25[0-5])\.(\d{1,2}|1\d\d|2[0-4]\d|25[0-5])\.(\d{1,2}|1\d\d|2[0-4]\d|25[0-5])\.(\d{1,2}|1\d\d|2[0-4]\d|25[0-5])$/;
var reg = obj.match(exp);
if(reg==null)
{
return false;
}
else
{
return true;
}
}
function CreateButtons(type,butname,fntn,btnid,optn)
{
butname = trans(butname);
if((optn == "off") ||(optn == ""))
{
str = "<input type='"+type+"' class='buttonsOff' name='"+btnid+"' id='"+btnid+"' value='"+butname+"' onclick='"+fntn+"'>";
}
else if(optn == "on")
{
str = "<input type='"+type+"' class='buttonsOn'  id='"+btnid+"' name='"+btnid+"' title='"+butname+"' value='"+butname+"' onclick='"+fntn+"'>";
}
return str;
}
